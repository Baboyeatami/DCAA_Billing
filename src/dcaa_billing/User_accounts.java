    private void loadUsers() {
        try {

            tableModel.setRowCount(0);
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs;
            ps = c.prepareStatement("Select  idUseAccounts, Accountnumber, Fname, Lname, Mname,Level from useaccounts");
            rs = ps.executeQuery();
            while (rs.next()) {
                tableModel.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3) + " " + rs.getString(4) + " " + rs.getString(5), rs.getString(6)});
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }

