    void SaveNewAccount() {
        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs = null;
            int UaccountNo = 0;
            boolean exist = false;
            String pass = String.valueOf(Password.getPassword());
            String timeStamp = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime());
            //  String.format("%010d", OrNumber);

            ps = c.prepareStatement("select * from  useaccounts");
            rs = ps.executeQuery();
            while (rs.next()) {
                if (rs.getString("Username").equals(Username.getText())) {
                    JOptionPane.showMessageDialog(this, "User name already exist!");
                    exist = true;
                    break;
                } else {
                    exist = false;
                }
            }

            ps = c.prepareStatement("select count(Accountnumber) from  useaccounts");
            rs = ps.executeQuery();
            if (rs.next()) {
                UaccountNo = rs.getInt(1);

            }

            if (!exist) {
                ps.close();;
                rs.close();

                ps = c.prepareStatement("insert into useaccounts (Accountnumber, Fname, Lname, Mname, Username, Password, Level, Date, createtime, User) values ('" + String.format("%05d", UaccountNo) + "','" + Fname.getText() + "','" + Lname.getText() + "','" + Mname.getText() + "','" + Username.getText() + "','" + pass + "','" + userlevel.getSelectedItem() + "','" + timeStamp + "','" + timeStamp + "','2')");
                ps.execute();
                JOptionPane.showMessageDialog(this, "New user Created!");
                loadUsers();
                JOptionPane.showMessageDialog(this, "All Fields will be cleared.");
                clear();
                String user = String.valueOf(UaccountNo);
                Activity_log(user);
            }

        } catch (SQLException ex) {
            Logger.getLogger(User_accounts.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

