    void LoadDepartmentData() {
        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs = null;
            ps = c.prepareStatement("Select Department_Name, Description from category_department where  idCategory_Department='" + DepartmentID + "'");
            rs = ps.executeQuery();

            if (rs.next()) {
                DparmentrName.setText(rs.getString(1));
                Description.setText(rs.getString(2));

            }

        } catch (SQLException ex) {
            Logger.getLogger(Add_Department.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

