    void loadInformation(String id) throws ParseException {

        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs = null;
            UpdateID = id;

            ps = c.prepareStatement("Select  F_name, M_name, L_name, Name_extention, Birthday, BirthPlace, Sex, Status, GradeLevel, Religion,Address,MotherName,fatherName,Contact from student_info where id='" + id + "'");
            rs = ps.executeQuery();
            Clear_data();
            if (rs.next()) {
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(rs.getString(5));
                Fname.setText(rs.getString(1));
                Mname.setText(rs.getString(2));
                Lname.setText(rs.getString(3));
                NameEX.setText(rs.getString(4));
                Bdate.setDate(date);
                Gender.setName(rs.getString(7));
                StatusCombo.setSelectedItem(rs.getString(8));
                GradeLevel.setText(rs.getString(9));
                Religion.setText(rs.getString(10));
                Brt_place.setText(rs.getString(6));
                Address.setText(rs.getString(11));
                Mother_maiden.setText(rs.getString(12));
                Fathers_name.setText(rs.getString(13));
                ContactNo.setText(rs.getString(14));
            }

        } catch (SQLException ex) {
            Logger.getLogger(StudentInfo_new.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

