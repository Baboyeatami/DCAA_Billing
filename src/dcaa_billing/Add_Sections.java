    void Add_section() {
        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs = null;
            String timeStamp = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime());

            ps = c.prepareStatement("Insert into section (SectionName, Adviser, Description, createtime, User,SY) values ('" + SectionName.getText() + "','" + Adviser.getText() + "','" + Decription.getText() + "','" + timeStamp + "','" + User + "','" + idSydisplay.get(jComboBox1.getSelectedIndex()) + "')");
            ps.execute();
            JOptionPane.showMessageDialog(this, "New section successfuly created");
            dispose();

        } catch (SQLException ex) {
            Logger.getLogger(Section.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    void Update() {
        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            //ResultSet rs = null;

            String timeStamp = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime());

            ps = c.prepareStatement("Update section set SectionName='" + SectionName.getText() + "', Adviser='" + Adviser.getText() + "', Description='" + Decription.getText() + "',SY='" + idSydisplay.get(jComboBox1.getSelectedIndex()) + "', createtime='" + timeStamp + "',User='" + User + "'  where idSection='" + SectionID + "'");
            ps.execute();
            JOptionPane.showMessageDialog(this, "Changes saved!");
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(Add_Sections.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    void loaddata_forUpdate() {
        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs = null;
            ps = c.prepareStatement("Select idsection ,SectionName, Adviser, Description,SY from section where idsection='" + SectionID + "' ");
            rs = ps.executeQuery();
            if (rs.next()) {
                sectionID.setText(rs.getString(1));
                SectionName.setText(rs.getString(2));
                Adviser.setText(rs.getString(3));
                Decription.setText(rs.getString(4));
                jComboBox1.setSelectedIndex(idSydisplay.indexOf(rs.getString("SY")));

            }
        } catch (SQLException ex) {
            Logger.getLogger(Add_Sections.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

