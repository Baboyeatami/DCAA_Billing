    private void Cash1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Cash1ActionPerformed

        if (!Partial) {
            Cash = Double.parseDouble(Cash1.getText());
            invoice.Cash = Cash;
            invoice.CashTendered = Cash;
            setVisible(false);

            invoice.computeTrasaction();
            invoice.displayPayedBills();
            invoice.load_running_Total();

        } else {
            System.out.println("Many Partial mode in Cash input");
            Cash = Double.parseDouble(Cash1.getText());
            invoice.Cash = Cash;
            invoice.CashTendered = Cash;
            setVisible(false);

            invoice.Compute_partialPay();
            invoice.displayPayedBills();
            invoice.load_running_Total();

        }
    }//GEN-LAST:event_Cash1ActionPerformed

