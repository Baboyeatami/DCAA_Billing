    void set_Update(boolean update, String id, Discounts cc) {
        addDiscount.setText("Save Changes");
        main = cc;
        DiscountID = id;
        this.update = update;

    }

    void up_date_discount() {
        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs = null;
            ps = c.prepareStatement("Update discounts set Discount_Name='" + DiscountName.getText() + "', Value='" + Discount.getText() + "', Description='" + Description.getText() + "', Type='" + DiscountCombox.getSelectedIndex() + "' where idDiscounts='" + DiscountID + "'");
            ps.execute();
            JOptionPane.showMessageDialog(this, "Changes saved!");

        } catch (SQLException ex) {
            Logger.getLogger(Add_Discount.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    void load_discountData() {

        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs = null;

            ps = c.prepareStatement("Select Discount_Name, Value, Description, Type from discounts where idDiscounts='" + DiscountID + "'");
            rs = ps.executeQuery();

            if (rs.next()) {
                DiscountName.setText(rs.getString(1));
                Discount.setText(rs.getString(2));
                Description.setText(rs.getString(3));
                DiscountCombox.setSelectedIndex(rs.getInt(4));

            }
            main.loadDiscounts();

        } catch (SQLException ex) {
            Logger.getLogger(Add_Discount.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    void addDiscount() {
        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs = null;
            int idDiscount = 0;
            ps = c.prepareStatement("Select  count(idDiscounts) from discounts");
            rs = ps.executeQuery();
            if (rs.next()) {
                idDiscount = Integer.parseInt(rs.getString(1));
            }

            String timeStamp = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime());
            ps = c.prepareStatement("Insert into discounts (idDiscounts,Discount_Name,Value,Description,Type,createtime,User) values" + "('" + idDiscount + "','" + DiscountName.getText() + "','" + Double.parseDouble(Discount.getText()) + "','" + Description.getText() + "','" + DiscountCombox.getSelectedIndex() + "','" + timeStamp + "','2')");
            ps.execute();
            JOptionPane.showMessageDialog(this, "New Discoount Successfully added");

        } catch (SQLException ex) {
            System.out.println(ex);
        }

    }

